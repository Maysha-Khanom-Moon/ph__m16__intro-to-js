var price = 35;

price += 10; // price = price + 10
console.log(price);


price -= 20; // price = price -20
console.log(price);


price *= 2; // price = price * 2
console.log(price);


price /= 2; // price = price / 2
console.log(price);


price %= 2; // price = price % 2
console.log(price + '\n');



// addition: +
var eggPrice = 10;
var onionPrice = 20;
console.log(eggPrice + onionPrice);


// subtraction: -
console.log(onionPrice - eggPrice);


// multiplication: *
console.log(onionPrice * eggPrice);


// division: /
console.log(onionPrice / eggPrice);
console.log(eggPrice / 3); // yellow (number)
console.log(parseInt(eggPrice / 3));
console.log((eggPrice / 3).toFixed(2)); // white (string)


// remainder or modulo: %
console.log(onionPrice % eggPrice);
console.log(eggPrice % onionPrice); 